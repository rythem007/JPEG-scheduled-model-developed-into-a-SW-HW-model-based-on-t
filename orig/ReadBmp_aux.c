void InitGlobals()
{
#ifdef _Schedule
	//fprintf(schefp, "InitGlobals()\n");
#endif	
	MDUWide = (ImageWidth+7) >> 3;
	MDUHigh = (ImageHeight+7) >> 3;

	MDUPadWidth = (MDUWide << 3) - ImageWidth;
	MDUPadHeight = (MDUHigh << 3) - ImageHeight;
}

void ReadBmpBlock(int n)
{
	int pos, stride, i, j, k, l;
	int x, y, lastval;
	int bp;
	int temp;
#ifdef _Schedule
	//fprintf(schefp, "ReadBmpBlock()\n");
#endif	

	stride = MDUWide << 3;
	x = (n % MDUWide) << 3;
	y = (n / MDUWide) << 3;

	/* preload scanlines */
	if (x == 0) {
		temp = y+8;
		if (temp > ImageHeight) {
			l = 8-MDUPadHeight;
			pos = x + (BmpScanHeight-y-l) * BmpScanWidth;
		}
		else {
			pos = x + (BmpScanHeight-8-y) * BmpScanWidth;
		}

		ifp_ptr = pos+BmpFileHeader[4];

		temp = y+8;
		if (temp > ImageHeight) {
			bp = stride * (l-1);

			// ## smsoc:loop(0,8)
			for (i=0; i<l; i++) {
				temp = BmpScanWidth-1;
				// ## smsoc:loop(115,115)
				for (j=0;j<=temp;j++)
				{
					ScanBuffer[bp+j] = ifp[ifp_ptr];
					ifp_ptr ++;
				}
				if (MDUPadWidth) {
					lastval = ScanBuffer[bp+ImageWidth-1];
					// ## smsoc:loop(0,4)
					for (k=ImageWidth; k<stride; k++)
						ScanBuffer[bp+k] = lastval;
				}
				bp -= stride;
			}
			bp = stride * l;
			// ## smsoc:loop(0,7)
			for (i=0; i<MDUPadHeight; i++)
			{
				// ## smsoc:loop(120,120)
				for (k=0; k<stride; k++)
				{
					ScanBuffer[bp] = ScanBuffer[bp-stride];
					bp++;
				}
			}
		}
		else {
			bp = stride * 7;
			for (i=0; i<8; i++) {
				temp = BmpScanWidth-1;
				// ## smsoc:loop(115,115)
				for (j=0;j<=temp;j++)
				{
					ScanBuffer[bp+j] = ifp[ifp_ptr];
					ifp_ptr ++;
				}
				if (MDUPadWidth != 0) {
					lastval = ScanBuffer[bp+ImageWidth-1];
					// ## smsoc:loop(0,4)
					for (k=ImageWidth; k<stride; k++)
						ScanBuffer[bp+k] = lastval;
				}
				bp -= stride;
			}
		}
	}

	bp = x;
	k = 0;
	for (i=0; i<8; i++) {
		for (j=0; j<8; j++) {
			bmpinput[k++] = ScanBuffer[bp+j];
		} 
		bp += stride;
	} 
#ifdef _KPNSIM
	//printf("JPEG Encoder iteration : %d\n",iteration);
	iteration++;
#endif
} 

int IsBmpFile()
{
	int t = ('B'<<8) | 'M';
	int c = ReadWord();
#ifdef _Schedule
	//fprintf(schefp, "IsBmpFile()\n");
#endif	
	ifp_ptr=ifp_ptr-2;

	if(t==c)
		return 1;
	else
		return 0;
}

void ReadBmpHeader()
{
	int i;

#ifdef _Schedule
	//fprintf(schefp, "ReadBmpHeader()\n");
#endif	
	if (IsBmpFile()) 
	{
		BmpFileHeader[0] = ReadWord();
		BmpFileHeader[1] = ReadRevDWord();
		BmpFileHeader[2] = ReadRevWord();
		BmpFileHeader[3] = ReadRevWord();
		BmpFileHeader[4] = ReadRevDWord();
	
		BmpInfoHeader[0] = ReadRevDWord();
		BmpInfoHeader[1] = ReadRevDWord();
		BmpInfoHeader[2] = ReadRevDWord();
		BmpInfoHeader[3] = ReadRevWord();
		BmpInfoHeader[4] = ReadRevWord();
	
		if(BmpInfoHeader[0] > 12)
		{
			BmpInfoHeader[5] = ReadRevDWord();
			BmpInfoHeader[6] = ReadRevDWord();
			BmpInfoHeader[7] = ReadRevDWord();
			BmpInfoHeader[8] = ReadRevDWord();
			BmpInfoHeader[9] = ReadRevDWord();
			BmpInfoHeader[10] = ReadRevDWord();
	
			for (i=0; i<256; i++) {
				BmpColors[i][0] = ReadByte();
				BmpColors[i][1] = ReadByte();
				BmpColors[i][2] = ReadByte();
				ReadByte();
			}
		}
	
		if (BmpInfoHeader[4] == 24) {
			BmpScanWidth = ((BmpInfoHeader[1]*3 + 3) >> 2) << 2;
			BmpScanHeight = BmpInfoHeader[2];
		}
		else {
			BmpScanWidth = ((BmpInfoHeader[1] + 3) >> 2) << 2;
			BmpScanHeight = BmpInfoHeader[2];
		}
	}
}
	
int ReadRevWord()
{
	int c;
	int temp;
#ifdef _Schedule
	//fprintf(schefp, "ReadRevWord()\n");
#endif	
	temp = ifp_ptr;
	c = ifp[temp++];
	c |= ifp[temp++] << 8;
	ifp_ptr = temp;

	return c;
}

int ReadWord()
{
	int c;
	int temp;
#ifdef _Schedule
	//fprintf(schefp, "ReadWord()\n");
#endif	
	temp = ifp_ptr;
	c = ifp[temp++] << 8;
	c |= ifp[temp++];
	ifp_ptr = temp;
	return c;
}

int ReadByte()
{
	int temp;
#ifdef _Schedule
	//fprintf(schefp, "ReadByte()\n");
#endif	
	temp = ifp_ptr;
	ifp_ptr = temp + 1;
	return ifp[temp];
}

long ReadRevDWord()
{
	int temp;
	long c;
#ifdef _Schedule
	//fprintf(schefp, "ReadRevDWord()\n");
#endif
	temp = ifp_ptr;	
	c = ifp[temp++];
	c |= ifp[temp++] << 8;
	c |= ifp[temp++] << 16;
	c |= ifp[temp++] << 24;
	ifp_ptr = temp;
	return c;
}
